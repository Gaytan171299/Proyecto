    private void Crear_Nueva_TarjetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Crear_Nueva_TarjetaActionPerformed
        String comp=(String) Compañia.getSelectedItem();
        
        if(Numero_Tarjeta.getText().isEmpty()==false && Compañia.getSelectedItem()!="Opciones"  && Banco.getText().isEmpty()==false && Nombre.getText().isEmpty()==false && Numero_Seguridad.getText().isEmpty()==false && Tipo.getText().isEmpty()==false && Fecha.getText().isEmpty()==false){
            if(validarNumeros(Numero_Tarjeta.getText())==true && validarNumeros(Numero_Seguridad.getText())==true){
                if(validarTarjeta()==true && validarSeguridad()==true){
                    Ingresar_Tarjeta(Numero_Tarjeta.getText(),Numero_Seguridad.getText(),Nombre.getText(),comp,correo,Banco.getText(),Fecha.getText(),Tipo.getText());
                    vaciarCajas();
                    n.llenarMapa();
                    Tarjetas.llenarTabla();
                   
                }else{
                     JOptionPane.showMessageDialog(null, "El número de tarjeta debe de tener 16 digitos.");
                }
               }else{
                JOptionPane.showMessageDialog(null,"Verifique que el numero de tarjeta y el numero de seguridad sean datos numericos.");
            }
        
        }else{
            JOptionPane.showMessageDialog(null,"Verifique que todas las cajas de texto se encuentren llenas.");
        }
                         dispose();
            Tarjetas s=new Tarjetas();
                s.setLocationRelativeTo(null);
                s.setDefaultCloseOperation(EXIT_ON_CLOSE);
                s.setResizable(false);
                s.setVisible(true);        

   
    
    }//GEN-LAST:event_Crear_Nueva_TarjetaActionPerformed

