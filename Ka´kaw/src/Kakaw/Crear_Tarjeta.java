   private static boolean validarNumeros(String cadena){
    	try {
                Long.parseLong(cadena);
                return true;
    	} catch (NumberFormatException e){
               return false;
    	}
    }
   
    private  boolean validarTarjeta(){
        if(Numero_Tarjeta.getText().length()==16){
            return true;
        }else{
            return false;
        }  
    }
        private  boolean validarSeguridad(){
        if(Numero_Seguridad.getText().length()==3){
            return true;
        }else{
            return false;
        }  
    }
    private void vaciarCajas(){
        Numero_Tarjeta.setText("");
        Numero_Seguridad.setText("");
        Compañia.setSelectedItem("Opciones");
        Banco.setText("");
        Nombre.setText("");
        Fecha.setText("");
        Tipo.setText("");
 
    }
