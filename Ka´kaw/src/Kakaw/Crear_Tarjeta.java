 public void Ingresar_Tarjeta(String numeroTar, String numeroSeg, String nombreUs, String compañia, String correo,String banco,String fecha,String tipo){
        try{
            procedimiento = Conexion.getInstancia().getConexion().prepareCall("CALL sp_Agregar_Tarjeta(?,?,?,?,?,?,?,?)");
            procedimiento.setString("_Numero_de_Tarjeta", numeroTar);
            procedimiento.setString("_Numero_de_Seguridad", numeroSeg);
            procedimiento.setString("_Nombre_Usuario", nombreUs);
            procedimiento.setString("_Compañia", compañia);
            procedimiento.setString("_Correo_Electronico",correo);
            procedimiento.setString("_Banco", banco);
            procedimiento.setString("_Fecha_de_vencimiento",fecha);
            procedimiento.setString("_Tipo_de_Tarjeta",tipo);
            
            procedimiento.executeQuery();

        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
