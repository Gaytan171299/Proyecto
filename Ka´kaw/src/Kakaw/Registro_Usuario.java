    private void Crear_UsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Crear_UsuarioActionPerformed
        if(Clave.getText().isEmpty()==false && Nombre_Usuario.getText().isEmpty()==false && Apellido_Usuario.getText().isEmpty()==false && Correo_Electronico.getText().isEmpty()==false && Numero_Celular.getText().isEmpty()==false && Direccion.getText().isEmpty()==false && Fecha_Nacimiento.getText().isEmpty()==false){
            
                    Ingresar_Usuario(Clave.getText(),Nombre_Usuario.getText(),Apellido_Usuario.getText(),Correo_Electronico.getText(),Numero_Celular.getText(),Direccion.getText(),Fecha_Nacimiento.getText());
                    vaciarCajas(); 
                                    dispose();
            Ingreso s=new Ingreso();
                s.setLocationRelativeTo(null);
                s.setDefaultCloseOperation(EXIT_ON_CLOSE);
                s.setResizable(false);
                s.setVisible(true);
        }else{
        JOptionPane.showMessageDialog(null,"Asegurese de completar todos los datos de las casillas.");
        }
            

    }//GEN-LAST:event_Crear_UsuarioActionPerformed

