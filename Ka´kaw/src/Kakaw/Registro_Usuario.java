public void Ingresar_Usuario(String clave,String nombreUs,String apellidoUs,String correoE, String numeroC, String direccion, String fechaN){
    try{ 
        procedimiento = Conexion.getInstancia().getConexion().prepareCall("CALL sp_Agregar_Usuario(?,?,?,?,?,?,?)");
        procedimiento.setString("_Clave",clave);
        procedimiento.setString("_Nombre_Usuario",nombreUs);
        procedimiento.setString("_Apellidos_Usuario",apellidoUs);
        procedimiento.setString("_Correo_Electronico",correoE);
        procedimiento.setString("_Numero_Celular",numeroC);
        procedimiento.setString("_Direccion",direccion);  
        procedimiento.setString("_Fecha_De_Nacimineto",fechaN);
        
        procedimiento.executeQuery();

        }
        catch(Exception e){
           e.printStackTrace();
        }
    }
