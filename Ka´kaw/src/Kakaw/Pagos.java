public   void llenarMapa(){
        try{
            mapa.clear();
            procedimiento=Conexion.getInstancia().getConexion().prepareCall("CALL sp_Consoltar_Tarjeta(?)");
            procedimiento.setString(1,correo);
            resultado= procedimiento.executeQuery();
            i=0;
            while(resultado.next()){
               
                mapa.put(i,new Tarjeta(resultado.getString("Numero_de_Tarjeta"),
                resultado.getString("CompaÃ±ia"),resultado.getString("Banco"),
                resultado.getString("Tipo_de_Tarjeta")));
                i++;
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }

private static void borrarTabla(){
        for(int i=modelo.getRowCount()-1;i>=0;i--){
            modelo.removeRow(i);
        }
    }
    public static void llenarTabla(){
        borrarTabla();
        i=0;
        while(i<mapa.size()){
            fila= new String[4];
            fila[0] = mapa.get(i).getTarjeta();
            fila[1] = mapa.get(i).getBanco();
            fila[2] = mapa.get(i).getTipo();

            modelo.addRow(fila);
         
            i++;
        }       
    }
    public void Ingresar_Pago(int idTrans, String correoE, int tar){
        try{
            procedimiento = Conexion.getInstancia().getConexion().prepareCall("CALL sp_Agregar_Transaccion_Usuario(?,?,?)");
            procedimiento.setInt("_idTransaccion", idTrans);
            procedimiento.setString("_Monto", correoE);
            procedimiento.setInt("_idEmpresa", tar);

            procedimiento.executeQuery();

        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
